// getSingleOrder  = > /orders/:id

exports.getSingleOrder = catchAsyncErrors(async (req, res, next) => {
  const order = await Order.findById(req.params.id).populate(
    "user",
    "name email"
  );

  if (!order) {
    return next(new ErrorHander("No Order found with this ID", 404));
  }

  res.status(200).json({
    success: true,
    order,
  });
});

